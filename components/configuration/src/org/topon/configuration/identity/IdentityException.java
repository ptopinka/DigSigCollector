package org.topon.configuration.identity;

public class IdentityException extends Exception {

	public IdentityException() {
	}

	public IdentityException(String arg0) {
		super(arg0);
	}

	public IdentityException(Throwable arg0) {
		super(arg0);
	}

	public IdentityException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
